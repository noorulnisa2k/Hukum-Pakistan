					<p style="color:#000 !important;"><b>FAQ (Frequently Asked Questions)</b><br/><br/>
                    <b>Q1: How can I register as a donor?</b><br/>
The process to register as a donor is simple. Just follow these steps.<br/>
1.	Visit our website (which you already did) or download our mobile application.<br/>
2.	Create a new account by clicking on ‘Register’.<br/>
3.	Log in with your account details.<br/>
4.	Click on ‘Be a Donor’ and fill out the form with correct information<br/>
5.	You are all done. You will be notified when someone near you might need blood.<br/><br/>

<b>Q2: How can I register as a blood recipient?</b><br/>
The process to register as a blood recipient is simple. Just follow these steps.<br/>
1.	Visit our website (which you already did) or download our mobile application.<br/>
2.	Create a new account by clicking on ‘Register’.<br/>
3.	Log in with your account details.<br/>
4.	Click on ‘Need Blood?’ and fill out a short form with correct information<br/>
5.	You will get access to three donors at a time.<br/>
6.	Send requests for blood donation and wait.<br/>
7.	If any one of them declines, you will get access to another donor to send request.<br/>
8.	Continue this until you are served.<br/><br/>

<b>Q3: Will my donor information be available to public?</b><br/>
No! Nothing of your personal information will be available publicly. Only persons who need blood can see your name and the area you belong to. Although you will be placed in the blood group category according to your blood group, it is only when you accept a donation request that your contact information will be shown to the person who requested.<br/><br/>

<b>Q4: Can I opt out as a donor in future?</b><br/>
Of course. We do not force people to remain in our donor list for life. It is purely a volunteer effort on behalf of our donors to help us save lives and feel the honor of being a savior. You can remove your donor status anytime you want due to any reason. You just need to leave a reason for this.<br/><br/>

<b>Q5: What if I forget my account password?</b><br/>
There is no need to worry if you have forgot your password. Just click ‘Forgot Password’ and you will be sent a one-time password on your registered number to reset your password.<br/><br/>

<b>Q6: Do I need to create an account for availing any service?</b><br/>
Yes, for availing any service specially blood services, you need to create an account to proceed further.<br/><br/>

<b>Q8: How will I know if someone needs blood?</b><br/>
After you register yourself as a donor, you will be notified through mobile application and SMS when a patient near you sends you request for donation. In this way, you will get to know if someone needs blood.<br/><br/>

<b>Q9: Can I change my location if I move to another city?</b><br/>
First and foremost, you will not need to change your location whenever you move to another city, because your location for blood donation will be shown based on GPS (Global Positioning System). However, still you can edit your information any time you want.<br/><br/>

<b>Q10: What is the process for getting blood?</b><br/>
After you have registered yourself and filled a ‘need blood’ form, you just need to send requests to available donors and wait if someone responds.
See: How can I register as a blood recipient?<br/><br/>

<b>Q11: Do I need to give blood to get blood?</b><br/>
No, Hukum Pakistan do not have an exchange policy. In other words, you do not need to give blood to get blood. If any of our volunteer donors is available near you and willing to donate, you will not be asked to first donate and then get blood. You will be given blood as per your needs. However, to save lives, we would love your kind gesture if you volunteer to be a donor with us just like our volunteer donor who helped you in times of need.<br/><br/>

<b>Q12: What is the process of donating blood?</b><br/>
The process of donating blood is simple. After registering as a donor, you will get notified whenever someone near you sends you a request for donation. You will visit the place where the person who requested is waiting and donate blood. That’s it.<br/><br/>

<b>Q13: Is there any compensation for donating blood?</b><br/>
No, there is no compensation policy for donating blood on behalf of Pakistan Blood Network. It is purely a volunteer service to the nation to help save lives. We do not compensate volunteer donors and neither do we get any penny from those who receive blood. We strictly prohibit volunteers registered with us to ask for money from those whom they donate. If anyone is reported to have taken any penny from a needy recipient, legal action will be taken against the violator.<br/><br/>

<b>Q14: Is there any age for becoming a donor?</b><br/>
Yes, according to WHO (World Health Organization), the age should be between 18-65 years. Although, in some countries the prevalent law may permit 16-17 year-olds to donate if they meet the physical and hematological criteria.<br/><br/>

